package com.charles.data.baidu.data;

public class BaiduBean {
    private String search_name;

    private String name;

    private String path;//域名

    private String nature;//性质 个人  企业

    private String company; // 单位名称

    private String domainTime; //时间

    private String ip; //网站ip

    private String author; //作者

    private String registrant; //注册人

    private String recordNumber; //备案号

    private String recordNumberPath; // 备案号网络地址

    private String corporateRepresentative; //法人代表

    public String getSearch_name() {
        return search_name;
    }

    public void setSearch_name(String search_name) {
        this.search_name = search_name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getNature() {
        return nature;
    }

    public void setNature(String nature) {
        this.nature = nature;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getDomainTime() {
        return domainTime;
    }

    public void setDomainTime(String domainTime) {
        this.domainTime = domainTime;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getRegistrant() {
        return registrant;
    }

    public void setRegistrant(String registrant) {
        this.registrant = registrant;
    }

    public String getRecordNumber() {
        return recordNumber;
    }

    public void setRecordNumber(String recordNumber) {
        this.recordNumber = recordNumber;
    }

    public String getRecordNumberPath() {
        return recordNumberPath;
    }

    public void setRecordNumberPath(String recordNumberPath) {
        this.recordNumberPath = recordNumberPath;
    }

    public String getCorporateRepresentative() {
        return corporateRepresentative;
    }

    public void setCorporateRepresentative(String corporateRepresentative) {
        this.corporateRepresentative = corporateRepresentative;
    }
}
