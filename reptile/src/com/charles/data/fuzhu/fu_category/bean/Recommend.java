package com.charles.data.fuzhu.fu_category.bean;

public class Recommend {

    private int novelId;
    private int novelChapters;
    private String novelName;
    private String novelSrc;
    private String novelImageUrl;
    private String novelAuthor;
    private long novelTime;
    private String novelCategory;

    public int getNovelId() {
        return novelId;
    }

    public void setNovelId(int novelId) {
        this.novelId = novelId;
    }

    public int getNovelChapters() {
        return novelChapters;
    }

    public void setNovelChapters(int novelChapters) {
        this.novelChapters = novelChapters;
    }

    public String getNovelName() {
        return novelName;
    }

    public void setNovelName(String novelName) {
        this.novelName = novelName;
    }

    public String getNovelSrc() {
        return novelSrc;
    }

    public void setNovelSrc(String novelSrc) {
        this.novelSrc = novelSrc;
    }

    public String getNovelImageUrl() {
        return novelImageUrl;
    }

    public void setNovelImageUrl(String novelImageUrl) {
        this.novelImageUrl = novelImageUrl;
    }

    public String getNovelAuthor() {
        return novelAuthor;
    }

    public void setNovelAuthor(String novelAuthor) {
        this.novelAuthor = novelAuthor;
    }

    public long getNovelTime() {
        return novelTime;
    }

    public void setNovelTime(long novelTime) {
        this.novelTime = novelTime;
    }

    public String getNovelCategory() {
        return novelCategory;
    }

    public void setNovelCategory(String novelCategory) {
        this.novelCategory = novelCategory;
    }
}
